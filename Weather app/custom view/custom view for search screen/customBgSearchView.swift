import UIKit

class customBgSearchView: UIView {

    func createBgView() -> UIView {
        let v = UIView(frame: CGRect(x: 0, y: 0, width: frame.size.width, height: frame.size.height))
        v.backgroundColor = .white
        return v
    }

    
}
